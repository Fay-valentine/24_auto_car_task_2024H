#include "Kalman.h"
#include "AllHeader.h"

Kalman kfp_motor;

void Kalman_Init(Kalman *kalman)
{
	kalman->Last_P = 1;			
	kalman->Now_P = 0;		
	kalman->out = 0;			
	kalman->Kg = 0;		
	kalman->Q = 0.009;//ﾔｽ魹ｽ魹ｽﾔｽ魹ｽ魹ｽﾋｳ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾓｦﾔｽ魹ｽ魹ｽ
	kalman->R = 0.10;
	kalman->source = 0;
	kalman->after_kalman = 0;
}

/**
 *魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾋｲ魹ｽ魹ｽ魹ｽ
 *@param 	Kalman *kfp 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ盪ｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
 *   			float input 魹ｽ魹ｽﾒｪ魹ｽﾋｲ魹ｽ魹ｽﾄｲ魹ｽ魹ｽ魹ｽ魹ｽﾄｲ魹ｽ魹ｽ魹ｽﾖｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｲﾉｼ魹ｽﾖｵ魹ｽ魹ｽ
 *@return 魹ｽﾋｲ魹ｽ魹ｽ魹ｽﾄｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ魹ｽ魹ｽ
 */
float KalmanFilter(Kalman *kfp,float input)
{
   //ﾔ､魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ賡ｽ魹ｽﾌ｣魹ｽkﾊｱ魹ｽ魹ｽﾏｵﾍｳ魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ魹ｽ = k-1ﾊｱ魹ｽﾌｵ魹ｽﾏｵﾍｳﾐｭ魹ｽ魹ｽ魹ｽ魹ｽ + 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ魹ｽ
   kfp->Now_P = kfp->Last_P + kfp->Q;
   //魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ豺ｽ魹ｽﾌ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ = kﾊｱ魹ｽ魹ｽﾏｵﾍｳ魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ魹ｽ / 魹ｽ魹ｽkﾊｱ魹ｽ魹ｽﾏｵﾍｳ魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ魹ｽ + 魹ｽﾛｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ詝ｩ
   kfp->Kg = kfp->Now_P / (kfp->Now_P + kfp->R);
   //魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ魹ｽ魹ｽ魹ｽﾌ｣魹ｽkﾊｱ魹ｽ魹ｽﾗｴﾌｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ = ﾗｴﾌｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾔ､魹ｽ魹ｽﾖｵ + 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ * 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ - ﾗｴﾌｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾔ､魹ｽ魹ｽﾖｵ魹ｽ魹ｽ
   kfp->out = kfp->out + kfp->Kg * (input -kfp->out);//魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾒｻ魹ｽﾎｵ魹ｽﾔ､魹ｽ魹ｽﾖｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｻ魹ｽﾎｵ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ
   //魹ｽ魹ｽ魹ｽ魹ｽﾐｭ魹ｽ魹ｽ魹ｽ賡ｽ魹ｽ魹ｽ: 魹ｽ魹ｽ魹ｽﾎｵ魹ｽﾏｵﾍｳﾐｭ魹ｽ魹ｽ魹ｽ賴ｶ魹ｽ魹ｽ kfp->LastP 魹ｽ魹ｽ魹ｽ魹ｽﾒｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾗｼ魹ｽ魹ｽ魹ｽ魹ｽ
   kfp->Last_P = (1-kfp->Kg) * kfp->Now_P;
   return kfp->out;
}